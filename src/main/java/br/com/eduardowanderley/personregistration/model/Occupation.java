package br.com.eduardowanderley.personregistration.model;

public class Occupation {

    private Long id;
    private String name;

    public Occupation() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
